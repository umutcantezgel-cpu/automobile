export function getVehicleById(id: string): Vehicle | undefined {
  return VEHICLES.find((v) => v.id === id);
}

export function filterVehicles(params: {
  make?: string;
  fuel?: string;
  priceMax?: number;
  priceMin?: number;
  kmMax?: number;
  category?: string;
}): Vehicle[] {
  let result = [...VEHICLES];

  if (params.make) {
    result = result.filter((v) => v.make.toLowerCase() === params.make!.toLowerCase());
  }
  if (params.fuel) {
    result = result.filter((v) => v.fuel.toLowerCase() === params.fuel!.toLowerCase());
  }
  if (params.priceMax) {
    result = result.filter((v) => v.price <= params.priceMax!);
  }
  if (params.priceMin) {
    result = result.filter((v) => v.price >= params.priceMin!);
  }
  if (params.kmMax) {
    result = result.filter((v) => v.km <= params.kmMax!);
  }
  if (params.category) {
    switch (params.category) {
      case 'neu':
        result = result.filter((v) => v.km < 5000);
        break;
      case 'jahres':
        result = result.filter((v) => v.km >= 5000 && v.km < 15000);
        break;
      case 'gebraucht':
        result = result.filter((v) => v.km >= 5000);
        break;
      case 'premium':
        result = result.filter((v) => v.isPremium);
        break;
    }
  }

  return result;
}
